export default {
  name: 'DongSearchInput',
  props: {
    modelValue: { type: String, default: '' },
    cityFilter: { type: String, default: '' },
    districtFilter: { type: String, default: '' },
    placeholder: { type: String, default: '예: 역삼동, 서초동' },
  },
  emits: ['update:modelValue', 'select'],
  data() {
    return {
      query: this.modelValue || '',
      results: [],
      showDropdown: false,
      activeIndex: -1,
    };
  },
  watch: {
    modelValue(val) {
      if (val !== this.query) this.query = val || '';
    },
  },
  mounted() {
    document.addEventListener('mousedown', this.onOutsideClick);
  },
  beforeUnmount() {
    document.removeEventListener('mousedown', this.onOutsideClick);
  },
  methods: {
    onInput() {
      this.activeIndex = -1;
      this.$emit('update:modelValue', this.query);
      this.search();
    },
    onFocus() {
      if (this.query) this.search();
      this.showDropdown = true;
    },
    search() {
      const q = this.query.trim();
      if (!q) {
        this.results = [];
        this.showDropdown = false;
        return;
      }
      this.showDropdown = true;
      let pool = DONG_DATA;
      if (this.cityFilter) pool = pool.filter(d => d.city === this.cityFilter);
      if (this.districtFilter) pool = pool.filter(d => d.district === this.districtFilter || d.district.startsWith(this.districtFilter));
      this.results = pool.filter(d => d.dong.includes(q)).slice(0, 20);
    },
    select(item) {
      this.query = item.dong;
      this.$emit('update:modelValue', item.dong);
      this.$emit('select', item);
      this.showDropdown = false;
      this.results = [];
    },
    clear() {
      this.query = '';
      this.$emit('update:modelValue', '');
      this.results = [];
      this.showDropdown = false;
    },
    onKeydown(e) {
      if (!this.showDropdown || !this.results.length) return;
      if (e.key === 'ArrowDown') {
        e.preventDefault();
        this.activeIndex = Math.min(this.activeIndex + 1, this.results.length - 1);
      } else if (e.key === 'ArrowUp') {
        e.preventDefault();
        this.activeIndex = Math.max(this.activeIndex - 1, 0);
      } else if (e.key === 'Enter' && this.activeIndex >= 0) {
        e.preventDefault();
        this.select(this.results[this.activeIndex]);
      } else if (e.key === 'Escape') {
        this.showDropdown = false;
      }
    },
    onOutsideClick(e) {
      if (this.$refs.dongWrap && !this.$refs.dongWrap.contains(e.target)) {
        this.showDropdown = false;
      }
    },
  },
};
</script>

